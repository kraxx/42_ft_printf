#include "ft_printf.h"

int	is_flag(char c)
{
	return (c == '-' || c == '+' || c == ' ' || c == '#' || c == '0');
}

int	is_mod(char c)
{
	return (c == 'h' || c == 'l' || c == 'j' || c == 'z' || c == 't');
}

int	is_spec(char c)
{
	return (c == 'c' || c == 'C' || c == 's' || c == 'S' ||
			c == 'd' || c == 'D' || c == 'i' || c == 'o' || c == 'O' ||
			c == 'x' || c == 'X' || c == 'u' || c == 'U' || c == 'p');
}
